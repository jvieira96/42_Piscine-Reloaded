int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
			x++;
		i++;
	}
	return (x);
}
