#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	x;
	int	y;

	x = 1;
	y = nb / 2;
	while (x < y)
	{
		if (x * x == nb)
			return (x);
		x++;
	}
	return (0);
}

